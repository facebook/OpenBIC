void pal_change_sdr_config_number_and_name(void)
{
	uint8_t card_position = get_card_position();
	uint8_t index;
	//Change sensor number and name of plat_sdr_table
	if (card_position == CARD_POSITION_2OU || card_position == CARD_POSITION_3OU ||
	    card_position == CARD_POSITION_4OU) {
		for (index = 0; index < ARRAY_SIZE(plat_sdr_table); index++) {
			plat_sdr_table[index].sensor_num +=
				(card_position * SENSOR_NUMBER_INTERVAL);

			if (!strcmp(change_name_table[index], "_BIC_MAIN_P12V_VOLT_V") &&
			    card_position == CARD_POSITION_3OU) {
				snprintf(plat_sdr_table[index].ID_str,
					 sizeof(change_name_table[index]), "3OU_P12V_EDGE_VOLT_V");
			} else {
				snprintf(plat_sdr_table[index].ID_str,
					 sizeof(change_name_table[index]), "%dOU%s",
					 (card_position + 1), change_name_table[index]);
			}
		}
	}
	//Change sensor number and name of EXP_name_table
	switch (card_position) {
	case CARD_POSITION_1OU:
	case CARD_POSITION_2OU:
		break;

	case CARD_POSITION_3OU:
		for (index = 0; index < ARRAY_SIZE(plat_EXPA_sdr_table); index++) {
			plat_EXPA_sdr_table[index].sensor_num +=
				((CARD_POSITION_3OU - CARD_POSITION_1OU) * SENSOR_NUMBER_INTERVAL);
			snprintf(plat_EXPA_sdr_table[index].ID_str,
				 sizeof(change_EXPA_name_table[index]), "%dOU%s",
				 (card_position + 1), change_EXPA_name_table[index]);
		}
		break;
	case CARD_POSITION_4OU:
		for (index = 0; index < ARRAY_SIZE(plat_EXPB_sdr_table); index++) {
			plat_EXPB_sdr_table[index].sensor_num +=
				((CARD_POSITION_4OU - CARD_POSITION_2OU) * SENSOR_NUMBER_INTERVAL);
			snprintf(plat_EXPB_sdr_table[index].ID_str,
				 sizeof(change_EXPB_name_table[index]), "%dOU%s",
				 (card_position + 1), change_EXPB_name_table[index]);
		}
		break;

	default:
		LOG_ERR("Can't identify board's position");
	}
}

uint8_t pal_get_extend_sdr()
{
	uint8_t extend_sdr_table_size = 0;
	uint8_t card_type = get_card_type();
	switch (card_type) {
	case CARD_TYPE_OPA:
		extend_sdr_table_size += ARRAY_SIZE(plat_EXPA_sdr_table);
		break;

	case CARD_TYPE_OPB:
		extend_sdr_table_size += ARRAY_SIZE(plat_EXPB_sdr_table);
		break;
	default:
		LOG_ERR("Unsupported card type, Card type: 0x%x", card_type);
		break;
	}

	return extend_sdr_table_size;
}

void pal_extend_full_sdr_table()
{
	uint8_t sdr_count = 0;
	uint8_t card_type = get_card_type();

	switch (card_type) {
	case CARD_TYPE_OPA:
		sdr_count = ARRAY_SIZE(plat_EXPA_sdr_table);
		for (int index = 0; index < sdr_count; index++) {
			add_full_sdr_table(plat_EXPA_sdr_table[index]);
		}
		break;

	case CARD_TYPE_OPB:
		sdr_count = ARRAY_SIZE(plat_EXPB_sdr_table);
		for (int index = 0; index < sdr_count; index++) {
			add_full_sdr_table(plat_EXPB_sdr_table[index]);
		}
		break;

	default:
		LOG_ERR("Unsupported card type, Card type: 0x%x", card_type);
		break;
	}
}

void load_sdr_table(void)
{
	pal_change_sdr_config_number_and_name();

	memcpy(full_sdr_table, plat_sdr_table, sizeof(plat_sdr_table));
	sdr_count = ARRAY_SIZE(plat_sdr_table);

	// Fix SDR table in different system/config
	pal_extend_full_sdr_table();
}
